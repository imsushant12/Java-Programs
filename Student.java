public class Student 
{
    private int roll;
    private String name;

    public void setName(String n)
    {
        name = n;
    }
    public void setRoll(int r)
    {
        roll = r;
    }

    public String getName()
    {
        return name;
    }
    public int getRoll()
    {
        return roll;
    }
}
